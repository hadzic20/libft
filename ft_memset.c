#include <stdlib.h>

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*ch;

	i = 0;
	ch = b;
	while (i < len)
		ch[i++] = c;
	b = ch;
	return (b);
}
